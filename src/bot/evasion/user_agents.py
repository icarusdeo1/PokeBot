def get_random_user_agent() -> str:
    """Return a randomly selected User-Agent string from the pool.

    Rotation strategy: uniform random selection per call. Each call is
    independent, which provides good entropy across repeated requests.
    """
    return random.choice(_USER_AGENT_POOL)


def get_user_agent_pool_size() -> int:
    """Return the number of User-Agent strings in the pool."""
    return len(_USER_AGENT_POOL)


def iter_user_agents() -> Iterator[str]:
    """Yield all User-Agent strings in the pool.

    Yields:
        Each User-Agent string in the pool, order preserved.
    """
    return iter(_USER_AGENT_POOL)


def get_user_agent_for_browser(browser: str) -> str:
    """Return a random User-Agent for the specified browser family.

    Args:
        browser: One of "chrome", "firefox", "safari", "edge", "opera",
                 "brave", "android", "iphone", "ipad".

    Returns:
        A randomly selected User-Agent string from the matching family.

    Raises:
        ValueError: If the browser family is not recognized.
    """
    family_map: dict[str, list[str]] = {
        "chrome": [ua for ua in _USER_AGENT_POOL if "Chrome/" in ua and "Edg/" not in ua and "OPR/" not in ua and "Brave" not in ua],
        "firefox": [ua for ua in _USER_AGENT_POOL if "Firefox/" in ua],
        "safari": [ua for ua in _USER_AGENT_POOL if "Safari/" in ua and "Chrome/" not in ua],
        "edge": [ua for ua in _USER_AGENT_POOL if "Edg/" in ua],
        "opera": [ua for ua in _USER_AGENT_POOL if "OPR/" in ua],
        "brave": [ua for ua in _USER_AGENT_POOL if "Brave" in ua],
        "android": [ua for ua in _USER_AGENT_POOL if "Android" in ua and "Mobile Safari" in ua],
        "iphone": [ua for ua in _USER_AGENT_POOL if "iPhone" in ua],
        "ipad": [ua for ua in _USER_AGENT_POOL if "iPad" in ua],
    }

    if browser not in family_map:
        raise ValueError(
            f"Unknown browser family '{browser}'. "
            f"Available: {', '.join(sorted(family_map.keys()))}"
        )

    pool = family_map[browser]
    if not pool:
        # Fallback to any mobile UA
        return get_random_user_agent()
    return random.choice(pool)
